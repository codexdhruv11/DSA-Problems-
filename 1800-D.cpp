int rightmostuppercase(const string& input, int endIndex) {
    endIndex = min(endIndex, static_cast<int>(input.length()) - 1);
    for (int i = endIndex; i >= 0; --i) {
        if (isupper(input[i])) {
            return i;
        }
    }
    return -1;
}

